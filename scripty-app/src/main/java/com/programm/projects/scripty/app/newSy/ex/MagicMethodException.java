package com.programm.projects.scripty.app.newSy.ex;

public class MagicMethodException extends Exception{

    public MagicMethodException() {
    }

    public MagicMethodException(String message) {
        super(message);
    }

    public MagicMethodException(String message, Throwable cause) {
        super(message, cause);
    }

    public MagicMethodException(Throwable cause) {
        super(cause);
    }

}
