package com.programm.projects.scripty.app.newSy.ex;

public class CommandInstantiationException extends Exception {

    public CommandInstantiationException() {
    }

    public CommandInstantiationException(String message) {
        super(message);
    }

    public CommandInstantiationException(String message, Throwable cause) {
        super(message, cause);
    }

    public CommandInstantiationException(Throwable cause) {
        super(cause);
    }
}
